package az.creational.patterns.prototype;

public class PersonPrototype implements Cloneable {

    private String name;
    private int age;


    public PersonPrototype(String name, int age) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public PersonPrototype clone() {
        try {
            PersonPrototype clone = (PersonPrototype) super.clone();
            // TODO: copy mutable state here, so the clone can't change the internals of the original
            return clone;
        } catch (CloneNotSupportedException e) {
            throw new AssertionError();
        }
    }
}
